public class Fijuras3D {
    float area;
    float base;
    float radio;
    float altura;
    float lado;

    public Fijuras3D(float area, float base, float radio, float altura, float lado) {
        this.area = area;
        this.base = base;
        this.radio = radio;
        this.altura = altura;
        this.lado = lado;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public float getBase() {
        return base;
    }

    public void setBase(float base) {
        this.base = base;
    }

    public float getRadio() {
        return radio;
    }

    public void setRadio(float radio) {
        this.radio = radio;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public float getLado() {
        return lado;
    }

    public void setLado(float lado) {
        this.lado = lado;
    }
}


